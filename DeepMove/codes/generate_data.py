import argparse
import pandas as pd
import numpy as np

def generate_data(n_trajs, traj_len=48, seed=42):
    np.random.seed(seed)
    data = []
    for _ in range(n_trajs):
        traj_id = np.random.randint(10000000, 99999999)
        lat_start = np.random.uniform(-90, 90)
        lon_start = np.random.uniform(-180, 180)
        latitudes = lat_start + np.cumsum(np.random.normal(0, 0.01, traj_len))
        longitudes = lon_start + np.cumsum(np.random.normal(0, 0.01, traj_len))
        timestamps = pd.date_range(start='1900-01-01', periods=traj_len, freq='30min')
        for i in range(traj_len):
            data.append([f'{traj_id}', latitudes[i], longitudes[i], timestamps[i]])
    df = pd.DataFrame(data, columns=['tid', 'lat', 'lon', 'timestamp'])
    return df

def generate_data_limited_locations(n_trajs, traj_len=48, seed=42, n_locations=10):
    np.random.seed(seed)
    start_lat = 22.5
    start_lon = 113.8
    end_lat = 22.8
    end_lon = 114.1
    locations = np.column_stack((
        np.random.uniform(start_lat, end_lat, n_locations),
        np.random.uniform(start_lon, end_lon, n_locations)
    ))
    data = []
    for _ in range(n_trajs):
        traj_id = np.random.randint(10000000, 99999999)
        loc_indices = np.random.choice(n_locations, traj_len)
        timestamps = pd.date_range(start='1900-01-01', periods=traj_len, freq='30min')
        for i in range(traj_len):
            lat, lon = locations[loc_indices[i]]
            data.append([f'{traj_id}', lat, lon, timestamps[i]])
    df = pd.DataFrame(data, columns=['tid', 'lat', 'lon', 'timestamp'])
    return df

